"""Sweep validation thresholds over an existing evaluation output.

Reads a `predictions.csv` file produced by `evaluate_pilot.py` or
`evaluate_end_to_end.py` and recomputes the operating point for multiple
thresholds without rerunning the pipeline.

This is useful to study the trade-off between:
- auto-validation rate
- auto-validation precision
- review rate

Examples:
    python scripts/eval/sweep_validation_threshold.py ^
        --predictions logs/pilot_eval/predictions.csv

    python scripts/eval/sweep_validation_threshold.py ^
        --predictions logs/end_to_end_eval/predictions.csv ^
        --thresholds 0.6,0.65,0.7,0.75,0.8
"""

from __future__ import annotations

import argparse
import csv
import json
from datetime import datetime
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[3]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Sweep validation thresholds over an existing predictions.csv.",
    )
    parser.add_argument(
        "--predictions",
        required=True,
        help="Path to predictions.csv generated by an evaluation script.",
    )
    parser.add_argument(
        "--output-dir",
        default=str(PROJECT_ROOT / "logs" / "threshold_sweep"),
        help="Directory where sweep artifacts will be written.",
    )
    parser.add_argument(
        "--thresholds",
        default=None,
        help="Comma-separated thresholds (e.g. 0.6,0.65,0.7,0.75).",
    )
    parser.add_argument(
        "--start",
        type=float,
        default=0.5,
        help="Start threshold when --thresholds is not provided (default: 0.5).",
    )
    parser.add_argument(
        "--stop",
        type=float,
        default=0.95,
        help="Stop threshold when --thresholds is not provided (default: 0.95).",
    )
    parser.add_argument(
        "--step",
        type=float,
        default=0.05,
        help="Step when --thresholds is not provided (default: 0.05).",
    )
    return parser.parse_args()


def parse_bool(value: str | None) -> bool:
    return str(value).strip().lower() in {"true", "1", "yes"}


def parse_float(value: str | None) -> float | None:
    text = str(value).strip()
    if not text:
        return None
    return float(text)


def safe_rate(num: int, den: int) -> float | None:
    if den == 0:
        return None
    return round(num / den, 4)


def load_predictions(path: Path) -> list[dict]:
    rows: list[dict] = []
    with path.open("r", encoding="utf-8-sig", newline="") as fh:
        reader = csv.DictReader(fh)
        for row in reader:
            rows.append({
                "business_match": parse_bool(row.get("business_match")),
                "predicted_norm": str(row.get("predicted_norm", "")).strip() or None,
                "global_confidence": parse_float(row.get("global_confidence")),
                "status": str(row.get("status", "")).strip(),
                "image_found": parse_bool(row.get("image_found", "True")),
            })
    return rows


def expand_thresholds(args: argparse.Namespace) -> list[float]:
    if args.thresholds:
        return [round(float(item.strip()), 4) for item in args.thresholds.split(",") if item.strip()]

    thresholds: list[float] = []
    current = args.start
    while current <= args.stop + 1e-9:
        thresholds.append(round(current, 4))
        current += args.step
    return thresholds


def recompute_status(row: dict, threshold: float) -> str:
    if not row["image_found"]:
        return "missing_image"
    if row["status"] == "no_detection":
        return "no_detection"
    if row["predicted_norm"] is None:
        return "needs_review"
    if row["global_confidence"] is None:
        return "needs_review"
    return "valid" if row["global_confidence"] >= threshold else "needs_review"


def sweep_rows(rows: list[dict], thresholds: list[float]) -> list[dict]:
    found_rows = [row for row in rows if row["image_found"]]
    total_found = len(found_rows)
    total_matches = sum(row["business_match"] for row in found_rows)
    total_readable = sum(row["predicted_norm"] is not None for row in found_rows)

    results: list[dict] = []
    for threshold in thresholds:
        status_rows = []
        for row in found_rows:
            status_rows.append({
                **row,
                "recomputed_status": recompute_status(row, threshold),
            })

        valid_rows = [row for row in status_rows if row["recomputed_status"] == "valid"]
        review_rows = [row for row in status_rows if row["recomputed_status"] == "needs_review"]
        no_detection_rows = [row for row in status_rows if row["recomputed_status"] == "no_detection"]
        valid_correct = [row for row in valid_rows if row["business_match"]]
        valid_incorrect = [row for row in valid_rows if not row["business_match"]]
        correct_but_reviewed = [
            row for row in review_rows
            if row["business_match"]
        ]

        results.append({
            "threshold": threshold,
            "match_rate": safe_rate(total_matches, total_found),
            "readable_rate": safe_rate(total_readable, total_found),
            "auto_validation_rate": safe_rate(len(valid_rows), total_found),
            "auto_validation_precision": safe_rate(len(valid_correct), len(valid_rows)),
            "auto_validation_error_rate": safe_rate(len(valid_incorrect), len(valid_rows)),
            "review_rate": safe_rate(len(review_rows), total_found),
            "no_detection_rate": safe_rate(len(no_detection_rows), total_found),
            "correct_but_reviewed_rate": safe_rate(len(correct_but_reviewed), total_found),
            "valid_count": len(valid_rows),
            "valid_correct": len(valid_correct),
            "valid_incorrect": len(valid_incorrect),
            "review_count": len(review_rows),
            "no_detection_count": len(no_detection_rows),
        })

    return results


def render_summary_md(predictions_path: Path, sweep_rows: list[dict]) -> str:
    def pct(value: float | None) -> str:
        if value is None:
            return "N/A"
        return f"{value:.2%}"

    lines = [
        "# Barrida de Threshold de Validacion",
        "",
        f"- Fecha: {datetime.now().isoformat(timespec='seconds')}",
        f"- Fuente: `{predictions_path}`",
        "",
        "| Threshold | Auto-validation rate | Precision | Error rate | Review rate | No-detection rate |",
        "| ---: | ---: | ---: | ---: | ---: | ---: |",
    ]

    for row in sweep_rows:
        lines.append(
            f"| {row['threshold']:.2f} | {pct(row['auto_validation_rate'])} | "
            f"{pct(row['auto_validation_precision'])} | {pct(row['auto_validation_error_rate'])} | "
            f"{pct(row['review_rate'])} | {pct(row['no_detection_rate'])} |"
        )

    lines.extend([
        "",
        "La barrida recalcula el estado `valid` vs `needs_review` a partir de",
        "`global_confidence`, sin volver a ejecutar el pipeline.",
        "",
    ])
    return "\n".join(lines)


def write_csv(path: Path, rows: list[dict], fieldnames: list[str]) -> None:
    with path.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)


def main() -> None:
    args = parse_args()
    predictions_path = Path(args.predictions)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    if not predictions_path.exists():
        raise FileNotFoundError(f"predictions.csv not found: {predictions_path}")

    thresholds = expand_thresholds(args)
    rows = load_predictions(predictions_path)
    sweep = sweep_rows(rows, thresholds)

    fieldnames = [
        "threshold",
        "match_rate",
        "readable_rate",
        "auto_validation_rate",
        "auto_validation_precision",
        "auto_validation_error_rate",
        "review_rate",
        "no_detection_rate",
        "correct_but_reviewed_rate",
        "valid_count",
        "valid_correct",
        "valid_incorrect",
        "review_count",
        "no_detection_count",
    ]

    write_csv(output_dir / "threshold_sweep.csv", sweep, fieldnames)

    with (output_dir / "threshold_sweep.json").open("w", encoding="utf-8") as fh:
        json.dump(sweep, fh, indent=2, ensure_ascii=False)

    with (output_dir / "threshold_sweep.md").open("w", encoding="utf-8") as fh:
        fh.write(render_summary_md(predictions_path, sweep))

    print("Threshold sweep finished.", flush=True)
    print(f"- threshold_sweep.csv: {output_dir / 'threshold_sweep.csv'}", flush=True)
    print(f"- threshold_sweep.json: {output_dir / 'threshold_sweep.json'}", flush=True)
    print(f"- threshold_sweep.md: {output_dir / 'threshold_sweep.md'}", flush=True)


if __name__ == "__main__":
    main()
